package com.guimolinas.flaggame;

public class ItemPais {

    String nome;
    int imagem;

    public ItemPais(String nome, int imagem) {
        this.nome = nome;
        this.imagem = imagem;
    }

    public String getNome() {
        return nome;
    }

    public int getImagem() {
        return imagem;
    }
}
